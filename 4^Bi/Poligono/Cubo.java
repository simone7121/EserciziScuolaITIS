public class Cubo extends Poligono {

    protected double peso;

    public Cubo(Double x) {
        super(4, x);
        peso = 0.0;
    }

    public void setPeso(double p) {
        peso = p;
    }

    public double volume() {
        return lato * lato * lato;
    }

}
